#include <map>
#include <vector>
#include <algorithm>
using namespace std;

// 【核心 1】手写 cmp 函数，针对 pair 进行多关键字排序
bool cmp(const pair<string, int>& a, const pair<string, int>& b) {
    if (a.second != b.second) {
        return a.second > b.second; // 第一关键字：按数量(Value)从大到小排
    }
    return a.first < b.first;       // 第二关键字：数量相同时，按名字(Key)从小到大排
}

int main() {
    map<string, int> mp;
    
    // 1. 正常存入数据并统计
    mp["apple"] = 5;
    mp["banana"] = 2;
    mp["cherry"] = 5;
    
    // 【核心 2】将 map 完美转存入 vector
    // 这一句可以把 map 里的所有 pair 瞬间拷进 vector 里
    vector<pair<string, int>> vec(mp.begin(), mp.end());

    // 【核心 3】对 vector 调用 sort
    sort(vec.begin(), vec.end(), cmp);

    // 4. 遍历 vector 输出排好序的结果
    for (auto& p : vec) {
        cout << p.first << " : " << p.second << endl;
    }
    return 0;
}
